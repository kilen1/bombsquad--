#!/usr/bin/env python3
# Скрипт для демонстрации новой функции смены имени хоста сервера

"""
Этот скрипт показывает, как использовать новую функцию смены имени хоста в сервере BombSquad.

Новые возможности:
1. Добавлено поле custom_hostname в ServerConfig
2. Изменена логика в ballisticakit_server.py для использования custom_hostname
3. Добавлен метод set_hostname() для динамической смены имени в интерактивном режиме
"""

print("Демонстрация новой функции смены имени хоста сервера BombSquad")
print("=" * 60)

print("\n1. Что нового:")
print("   - Добавлено поле 'custom_hostname' в конфигурацию сервера")
print("   - Теперь можно задать кастомное имя хоста, отличающееся от имени партии")
print("   - Это имя будет использоваться как имя устройства при подключении к мастер-серверу")

print("\n2. Как использовать в конфигурационном файле (config.toml):")
print("   # Name of our server in the public parties list.")
print("   party_name = \"Mod\"")
print("   ")
print("   # Custom hostname for the server (if different from party_name).")
print("   # This will be used as the device name for connection purposes.")
print("   custom_hostname = \"MyCustomServerName\"")

print("\n3. Как использовать в интерактивном режиме:")
print("   - Запустите сервер в интерактивном режиме: ./bombsquad_server")
print("   - Используйте команду: mgr.set_hostname('НовоеИмяСервера')")
print("   - Имя хоста изменится (применится при следующем запуске подпроцесса)")

print("\n4. Важные особенности:")
print("   - Имя устройства меняется только при перезапуске подпроцесса сервера")
print("   - Изменение через mgr.set_hostname() временно, пока не перезапишется в config.toml")
print("   - Если custom_hostname не задан, используется party_name")

print("\n5. Примеры использования:")
print("   - Смена имени для лучшей идентификации сервера в системе")
print("   - Использование кастомных имен для разных инстансов сервера")
print("   - Скрытие стандартных имен вроде 'server23213213'")

print("\nГотово! Новая функция позволяет гибко управлять именем хоста сервера.")